"""---
## Figure 1 — Label posteriors sharpen with $t$

**What to write in the paper:**

> The time-dependent kernel $\kappa_t$ has a natural calibration property: at $t \approx 0$ the bandwidth is large and posteriors are near-uniform, reflecting the absence of information early in the denoising process. As $t \to 1$ the bandwidth shrinks and posteriors concentrate correctly on each class. This calibration is **automatic** — it follows directly from the flow matching probability path without any hyperparameter tuning. Figure~X illustrates this on two moons with $M=5$ reference images per class (shown as circles and triangles). The decision boundary (white contour at $\hat{p}_t = 0.5$) sharpens over time and converges to the true class boundary by $t = 0.92$.

**Key point for reviewers:** the kernel is not designed to do label propagation — it *emerges* from the optimal flow matching drift. The fact that it also works as a label propagator is the theoretical contribution.
"""

